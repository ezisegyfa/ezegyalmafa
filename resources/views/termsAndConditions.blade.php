
<BR><h2>Felhasználási feltételek</h2>
<BR>
<BR>A Tűzifa Telep Kft, CUI 37648118, reg. szám J19/431/2017, a továbbiakban Működtető weboldalának (http://fatelep.ro, továbbiakban Weboldal) használatával Ön (továbbiakban Felhasználó) elfogadja a Felhasználási feltételeket.
<BR>
<BR>Megrendelés
<BR>
<BR>1.1.   A weboldalon keresztül az Ügyfél/Vásárló elektronikus úton Megrendelést adhat le oly módon, hogy a kiválasztott Termékre kattintva a formon megadja a Megrendelés teljesítéséhez szükséges szállítási és számlázási adatokat. A Termékre kattintás nem jelenti a Megrendelés véglegesítését.
<BR>
<BR>1.2.  Az Ügyfél jelen Felhasználási feltételek elfogadását követően a Megrendelés gombra kattintva véglegesítheti a Megrendelést (ajánlat).
<BR>
<BR> 1.3. A Megrendelés véglegesítésével az Ügyfél/Vásárló kijelenti, hogy az általa megadott, a vásárláshoz szükséges adatok a valóságnak megfelelnek, és hozzájárul, hogy azokat az Eladó Megrendelés teljesítése céljából felhasználja, kezelje, különösen, hogy szükség esetén, az Eladó felvegye a kapcsolatot vele a megadott elérhetőségek (email vagy telefon) bármelyikén. A Megrendelés véglegesítésével az Ügyfél vállalja, hogy a Megrendelés ellenértékét kiegyenlíti a termék átadásakor, ennek elmaradása esetén a Megrendelés teljesítését az Eladó megtagadhatja.
<BR>
<BR>2. A Termékek feletti tulajdonjog átszállása
<BR> 
<BR>2.1. A Termékek feletti tulajdonjog a Termék átvételekor száll át a Vásárlóra, amennyiben a Vásárló Termék vételárát hiánytalanul megfizette.
<BR>
<BR>3. Szállítás
<BR>
<BR>3.1 A kiszállítás elsősorban a rendszerbe táplált, kiválasztható településekre történik. Ezektől eltérő település esetén kérjük telefonon egyeztessen munkatársunkkal az 0745 253 737 telefonszámon.
<BR>
<BR>3.2. A termék ára tartalmazza a szállítást a betáplált települések esetén. Más településekre szállításkor a Működtető fenntartja a jogot szállítási díj kiszabására, amelyről előzetesen tájékoztatja a Felhasználót.
<BR>
<BR>4. Számlázás és fizetés
<BR>
<BR>4.1 A fizetés a termék átvételekor történik, készpénzzel.
<BR>
<BR>4.2. A számla az ön által megadott adatok alapján állítódik ki.
<BR>
<BR>4.3. Az oldalon szereplő árak RON-ban értendőek és tartalmazzák az ÁFÁ-t (TVA).
<BR>
<BR>5. Személyes adatok védelme
<BR>
<BR>5.1. A Személyes adatok védelmére vonatkozó feltételeket a weboldal Személyes adatok védelme oldalán találja.
<BR>
<BR>
<BR>6. Alkalmazandó jog, panaszkezelés, jogérvényesítés
<BR> 
<BR>
<BR>6.1. Jelen Szerződés vonatkozásában a román jog az irányadó.
<BR>
<BR>6.2. A fogyasztókra vonatkozó jogszabályok tekintetében a román jog az irányadó.
<BR>
<BR>6.3. A Működtető fenntartja a jogot, hogy jelen Weboldal Felhasználási Feltételek rendelkezéseit frissítse, illetve időközönként módosítsa, hogy az tükrözze a weboldal működési módját és feltételeit, vagy a jogszabályi környezet változását. Ilyen esetekben az Felhasználási Feltételek módosított változata jelenik meg az oldalon. Az Általános Felhasználási Feltételek tartalmát érdemes időről-időre figyelni a Weboldalon. Az Általános Felhasználási Feltételek ellen, azok Weboldalon történő közzétételét követően, a Felhasználó tiltakozhat.
